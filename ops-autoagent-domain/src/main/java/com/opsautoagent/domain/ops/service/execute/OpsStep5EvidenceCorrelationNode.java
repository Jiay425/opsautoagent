    private String buildReviewMessage(OpsEvidenceReviewResult reviewResult) {
        if (reviewResult == null) {
            return "Evidence Reviewer Agent returned no review result.";
        }
        return "Evidence Reviewer Agent status=" + reviewResult.getStatus()
                + ", conclusionType=" + reviewResult.getConclusionType()
                + ", round=" + reviewResult.getRound()
                + ", confidence=" + reviewResult.getConfidenceScore()
                + "\nRoot cause:\n" + blankToDefault(reviewResult.getRootCause(), "- none")
                + "\nConfirmed facts:\n" + join(reviewResult.getConfirmedFacts())
                + "\nWeak evidence:\n" + join(reviewResult.getWeakEvidence())
                + "\nMissing evidence:\n" + join(reviewResult.getMissingEvidence())
                + "\nRequired tools:\n" + join(reviewResult.getRequiredTools())
                + "\nReport constraints:\n" + join(reviewResult.getReportConstraints())
                + "\nRationale: " + reviewResult.getRationale();
    }

    private String formatEvidenceSignals(List<EvidenceSignalEntity> evidenceSignals) {
        if (evidenceSignals == null || evidenceSignals.isEmpty()) {
            return "No neutral evidence signals extracted.";
        }
        StringBuilder builder = new StringBuilder("Neutral evidence signals extracted. These are observations, not root-cause conclusions.\n");
        for (EvidenceSignalEntity signal : evidenceSignals) {
            builder.append("- ")
                    .append(signal.getSource()).append("/")
                    .append(signal.getEvidenceType()).append(" ")
                    .append(signal.getName()).append(" [")
                    .append(signal.getStatus()).append("] ")
                    .append(signal.getSummary()).append("\n");
        }
        return builder.toString();
    }

    private boolean available(MetricEvidenceEntity evidence) {
        return evidence != null && evidence.isAvailable();
    }

    private boolean available(LogEvidenceEntity evidence) {
        return evidence != null && evidence.isAvailable();
    }

    private boolean available(TraceEvidenceEntity evidence) {
        return evidence != null && evidence.isAvailable();
    }

    private String join(List<String> items) {
        if (items == null || items.isEmpty()) {
            return "- none";
        }
        StringBuilder builder = new StringBuilder();
        for (String item : items) {
            builder.append("- ").append(item).append("\n");
        }
        return builder.toString();
    }

    private boolean isBlank(String value) {
        return value == null || value.trim().isEmpty();
    }

    private String blankToDefault(String value, String defaultValue) {
        return isBlank(value) ? defaultValue : value;
    }

